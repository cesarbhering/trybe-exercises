// Faça um programa que retorne o maior de dois números. Defina no começo do programa duas variáveis com os valores que serão comparados.

let numA = 43;
let numB = 56;

if (numA > numB) {
    console.log(numA);
}
else if (numA < numB) {
    console.log(numB);
}
else if (numA == numB) {
    console.log("Números iguais");
}
else {
    console.log("Números inseridos inválidos");
}